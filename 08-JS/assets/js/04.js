//✍️ ~ L'Addition ~  //

// ✍️ Déclaration de plusieurs variables a la suite

var nb1,nb2,resultat;

nb1 = 10;
nb2 =  5;

// ✍️-- Addition de nb1 + nb2 avec l'operateur "+"
resultat = nb1 + nb2;

//✍️ Affichage du resultat dans la console
console.log("L'addition de nb1 et nb2 est égale à : " + resultat);

// ✍️ ~~ La Soustraction ~~ //

//✍️ La soustraction de nb1 et nb2 avec l'operateur "-"
resultat = nb1 - nb2;

//✍️ Affichage du resultat dans la console
console.log("La soustraction de nb1 et nb2 est égale à : " + resultat);

//✍️ ~~ La Multiplication ~~ //

//✍️ La multiplication de nb1 et nb2 avec l'operateur "*"
resultat = nb1 * nb2;

//✍️ Affichage du resultat dans la console

console.log("La multiplication de nb1 et nb2 est égale à : " + resultat);

//✍️ ~~ La Division ~~ //

//✍️ La Division de nb1 et nb2 avec l'operateur "/"
resultat = nb1 / nb2;

//✍️ Affichage du resultat dans la console

console.log("La division de nb1 et nb2 est égale à : " + resultat);


//✍️ ~~ Le Modulo ~~

// Le résultat du Modulo est le reste de la division
//✍️ La Modulo de nb1 et nb2 avec l'operateur "%"
nb1 = 11;
resultat = nb1 % nb2;

//✍️ Affichage du resultat dans la console

console.log("La Modulo de nb1 et nb2 est égale à : " + resultat);

